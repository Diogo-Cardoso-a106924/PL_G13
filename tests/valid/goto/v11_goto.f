PROGRAM SUB_TEST
  INTEGER A(5), N, I
  REAL X, Y, Z

  N = 5
  DO 10 I = 1, N
    A(I) = I
10 CONTINUE

  CALL PRINTARR(A, N)
  CALL SWAP(A(1), A(5))
  CALL PRINTARR(A, N)

  X = 3.0
  Y = 4.0
  CALL CALC(X, Y, Z)
  PRINT *, Z

  CALL NOARGS
END

SUBROUTINE PRINTARR(ARR, N)
  INTEGER ARR(5), N, I
  DO 10 I = 1, N
    PRINT *, ARR(I)
10 CONTINUE
END

SUBROUTINE SWAP(X, Y)
  INTEGER X, Y, TEMP
  TEMP = X
  X = Y
  Y = TEMP
END

SUBROUTINE CALC(X, Y, Z)
  REAL X, Y, Z
  Z = X ** 2 + Y ** 2
END

SUBROUTINE NOARGS
  PRINT *, 'NO ARGS'
END