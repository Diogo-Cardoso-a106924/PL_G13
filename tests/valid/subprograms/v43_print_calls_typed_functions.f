PROGRAM RETURN_TEST
  INTEGER SIGN_OF, ABS_VAL
  INTEGER X

  X = -5
  PRINT *, SIGN_OF(X)
  PRINT *, ABS_VAL(X)

  X = 7
  PRINT *, SIGN_OF(X)
  PRINT *, ABS_VAL(X)

  X = 0
  PRINT *, SIGN_OF(X)
END

INTEGER FUNCTION SIGN_OF(X)
  INTEGER X
  IF (X .GT. 0) THEN
    SIGN_OF = 1
    RETURN
  ELSEIF (X .LT. 0) THEN
    SIGN_OF = -1
    RETURN
  ENDIF
  SIGN_OF = 0
END

INTEGER FUNCTION ABS_VAL(X)
  INTEGER X
  IF (X .LT. 0) THEN
    ABS_VAL = -X
    RETURN
  ENDIF
  ABS_VAL = X
END
